#[cfg(test)]
mod tests {
    use super::*;
    use crate::siprec::metadata::{Participant, StreamInfo};

    fn test_config() -> SrsConfig {
        SrsConfig {
            enabled: true,
            recording_dir: "/tmp/siphon-test-recordings".to_string(),
            max_sessions: 100,
            backend: "file".to_string(),
            file: None,
            http: None,
            rtpengine_profile: "srs_recording".to_string(),
        }
    }

    fn test_metadata() -> RecordingMetadata {
        RecordingMetadata {
            session_id: "test-session-001".to_string(),
            participants: vec![
                Participant {
                    participant_id: "p1".to_string(),
                    aor: "sip:alice@example.com".to_string(),
                    name: None,
                },
                Participant {
                    participant_id: "p2".to_string(),
                    aor: "sip:bob@example.com".to_string(),
                    name: None,
                },
            ],
            streams: vec![
                StreamInfo {
                    stream_id: "s1".to_string(),
                    session_id: "test-session-001".to_string(),
                    label: "caller-audio".to_string(),
                },
            ],
        }
    }

    #[test]
    fn create_session_and_lookup() {
        let manager = SrsManager::new(test_config());
        let metadata = test_metadata();

        let result = manager.create_session("call-1", "from-tag-1", metadata);
        assert!(result.is_some());

        let (session_id, to_tag) = result.unwrap();
        assert_eq!(session_id, "test-session-001");
        assert!(to_tag.starts_with("srs-"));

        assert!(manager.is_srs_session("call-1"));
        assert!(!manager.is_srs_session("call-2"));
        assert_eq!(manager.session_count(), 1);
    }

    #[test]
    fn activate_session() {
        let manager = SrsManager::new(test_config());
        let metadata = test_metadata();

        let (session_id, _) = manager.create_session("call-1", "from-tag-1", metadata).unwrap();

        // Initially pending.
        let session = manager.get_session(&session_id).unwrap();
        assert_eq!(session.state, SrsSessionState::Pending);
        drop(session);

        // Activate.
        manager.activate_session(&session_id);
        let session = manager.get_session(&session_id).unwrap();
        assert_eq!(session.state, SrsSessionState::Recording);
    }

    #[test]
    fn stop_session_returns_record() {
        let manager = SrsManager::new(test_config());
        let metadata = test_metadata();

        let (session_id, _) = manager.create_session("call-1", "from-tag-1", metadata).unwrap();
        manager.activate_session(&session_id);

        let record = manager.stop_session("call-1");
        assert!(record.is_some());

        let record = record.unwrap();
        assert_eq!(record.session_id, "test-session-001");
        assert_eq!(record.participants.len(), 2);
        assert_eq!(record.state, "completed");

        // Session cleaned up.
        assert_eq!(manager.session_count(), 0);
        assert!(!manager.is_srs_session("call-1"));
    }

    #[test]
    fn fail_session() {
        let manager = SrsManager::new(test_config());
        let metadata = test_metadata();

        let (session_id, _) = manager.create_session("call-1", "from-tag-1", metadata).unwrap();
        manager.fail_session(&session_id, "RTPEngine unreachable");

        let session = manager.get_session(&session_id).unwrap();
        assert!(matches!(&session.state, SrsSessionState::Failed(reason) if reason == "RTPEngine unreachable"));
    }

    #[test]
    fn max_sessions_enforced() {
        let mut config = test_config();
        config.max_sessions = 2;
        let manager = SrsManager::new(config);

        // Create session 1.
        let metadata1 = RecordingMetadata {
            session_id: "sess-1".to_string(),
            participants: vec![],
            streams: vec![],
        };
        assert!(manager.create_session("call-1", "tag-1", metadata1).is_some());

        // Create session 2.
        let metadata2 = RecordingMetadata {
            session_id: "sess-2".to_string(),
            participants: vec![],
            streams: vec![],
        };
        assert!(manager.create_session("call-2", "tag-2", metadata2).is_some());

        // Session 3 should be rejected.
        let metadata3 = RecordingMetadata {
            session_id: "sess-3".to_string(),
            participants: vec![],
            streams: vec![],
        };
        assert!(manager.create_session("call-3", "tag-3", metadata3).is_none());
    }

    #[test]
    fn stop_nonexistent_session_returns_none() {
        let manager = SrsManager::new(test_config());
        assert!(manager.stop_session("no-such-call").is_none());
    }

    #[test]
    fn session_state_display() {
        assert_eq!(SrsSessionState::Pending.to_string(), "pending");
        assert_eq!(SrsSessionState::Recording.to_string(), "recording");
        assert_eq!(SrsSessionState::Stopping.to_string(), "stopping");
        assert_eq!(SrsSessionState::Completed.to_string(), "completed");
        assert_eq!(
            SrsSessionState::Failed("timeout".to_string()).to_string(),
            "failed: timeout"
        );
    }

    #[test]
    fn recording_record_serialization() {
        let manager = SrsManager::new(test_config());
        let metadata = test_metadata();

        let (session_id, _) = manager.create_session("call-1", "tag-1", metadata).unwrap();
        manager.activate_session(&session_id);

        let session = manager.get_session(&session_id).unwrap();
        let record = session.to_recording_record();

        let json = serde_json::to_string(&record).unwrap();
        assert!(json.contains("test-session-001"));
        assert!(json.contains("alice@example.com"));
        assert!(json.contains("caller-audio"));
    }

    #[test]
    fn recording_dir_per_session() {
        let manager = SrsManager::new(test_config());
        let metadata = test_metadata();

        let (session_id, _) = manager.create_session("call-1", "tag-1", metadata).unwrap();
        let dir = manager.recording_dir(&session_id).unwrap();
        assert_eq!(dir, PathBuf::from("/tmp/siphon-test-recordings/test-session-001"));
    }
}
