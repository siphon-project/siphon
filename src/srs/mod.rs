/// Manages all active SRS recording sessions.
#[derive(Debug)]
pub struct SrsManager {
    /// session_id → SrsSession
    sessions: DashMap<String, SrsSession>,
    /// recording_call_id → session_id (for routing BYE/re-INVITE by Call-ID)
    call_id_to_session: DashMap<String, String>,
    /// Configuration.
    config: SrsConfig,
}

impl SrsManager {
    /// Create a new SRS manager from config.
    pub fn new(config: SrsConfig) -> Self {
        Self {
            sessions: DashMap::new(),
            call_id_to_session: DashMap::new(),
            config,
        }
    }

    /// Register a new recording session from an inbound SIPREC INVITE.
    ///
    /// Returns the session ID and generated to-tag, or `None` if max sessions
    /// exceeded.
    pub fn create_session(
        &self,
        recording_call_id: &str,
        from_tag: &str,
        metadata: RecordingMetadata,
    ) -> Option<(String, String)> {
        // Check capacity.
        if self.sessions.len() >= self.config.max_sessions {
            warn!(
                max = self.config.max_sessions,
                current = self.sessions.len(),
                "SRS: max sessions reached, rejecting recording"
            );
            return None;
        }

        let session_id = metadata.session_id.clone();
        let to_tag = format!("srs-{}", &uuid::Uuid::new_v4().to_string()[..8]);

        // Build per-session recording directory.
        let recording_dir = PathBuf::from(&self.config.recording_dir).join(&session_id);

        let session = SrsSession {
            session_id: session_id.clone(),
            recording_call_id: recording_call_id.to_string(),
            original_call_id: None, // Set from metadata sipSessionID if needed
            from_tag: from_tag.to_string(),
            to_tag: to_tag.clone(),
            metadata,
            state: SrsSessionState::Pending,
            started_at: Instant::now(),
            ended_at: None,
            recording_dir: Some(recording_dir),
        };

        info!(
            session_id = %session_id,
            recording_call_id = %recording_call_id,
            participants = session.metadata.participants.len(),
            streams = session.metadata.streams.len(),
            "SRS: new recording session"
        );

        self.sessions.insert(session_id.clone(), session);
        self.call_id_to_session.insert(recording_call_id.to_string(), session_id.clone());

        Some((session_id, to_tag))
    }

    /// Mark a session as actively recording (after RTPEngine setup succeeds).
    pub fn activate_session(&self, session_id: &str) {
        if let Some(mut session) = self.sessions.get_mut(session_id) {
            session.state = SrsSessionState::Recording;
            info!(session_id = %session_id, "SRS: recording active");
        }
    }

    /// Mark a session as failed.
    pub fn fail_session(&self, session_id: &str, reason: &str) {
        if let Some(mut session) = self.sessions.get_mut(session_id) {
            session.state = SrsSessionState::Failed(reason.to_string());
            session.ended_at = Some(Instant::now());
            warn!(session_id = %session_id, reason = %reason, "SRS: recording failed");
        }
    }

    /// Handle BYE from SRC — stop the recording session.
    ///
    /// Returns the recording record for storage, or `None` if not found.
    pub fn stop_session(&self, recording_call_id: &str) -> Option<RecordingRecord> {
        let session_id = self.call_id_to_session.get(recording_call_id)?
            .clone();

        let record = if let Some(mut session) = self.sessions.get_mut(&session_id) {
            session.state = SrsSessionState::Completed;
            session.ended_at = Some(Instant::now());
            let record = session.to_recording_record();
            info!(
                session_id = %session_id,
                duration = record.duration_secs,
                "SRS: recording completed"
            );
            Some(record)
        } else {
            None
        };

        // Clean up mappings.
        self.call_id_to_session.remove(recording_call_id);
        self.sessions.remove(&session_id);

        record
    }

    /// Look up a session by the SIPREC INVITE's Call-ID.
    pub fn session_for_call_id(&self, recording_call_id: &str) -> Option<String> {
        self.call_id_to_session.get(recording_call_id).map(|value| value.clone())
    }

    /// Check if a Call-ID belongs to an active SRS session.
    pub fn is_srs_session(&self, call_id: &str) -> bool {
        self.call_id_to_session.contains_key(call_id)
    }

    /// Number of active recording sessions.
    pub fn session_count(&self) -> usize {
        self.sessions.len()
    }

    /// Get the SRS config.
    pub fn config(&self) -> &SrsConfig {
        &self.config
    }

    /// Get session details by session ID.
    pub fn get_session(&self, session_id: &str) -> Option<dashmap::mapref::one::Ref<'_, String, SrsSession>> {
        self.sessions.get(session_id)
    }

    /// Get the recording directory for a session.
    pub fn recording_dir(&self, session_id: &str) -> Option<PathBuf> {
        self.sessions.get(session_id)
            .and_then(|session| session.recording_dir.clone())
    }

    /// Get the RTPEngine profile name to use for recording.
    pub fn rtpengine_profile(&self) -> &str {
        &self.config.rtpengine_profile
    }
}

